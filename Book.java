
public class Book 
{
	public String bookName;
	public int bookPage;
	public String yazar ;
	public int year ;
	
	public Book(String bookName, int bookPage, String yazar, int year) 
	{
		super();
		this.bookName = bookName;
		this.bookPage = bookPage;
		this.yazar = yazar;
		this.year = year;
	}
	
	

}
